def nearby_organisms(cellgrid, x, y):
    # Keep count of nearby mines
    count = 0
    for i in range(-1, 2):
        for j in range(-1, 2):
            if x+i == -1 or y+j == -1:
                continue
            try:
                count += cellgrid[x + i][y + j]
            except IndexError:
                continue
    count -= cellgrid[x][y]
    return count


def update(surface, cur, sz, y, x):
    nxt = [[0 for x in range(x)] for y in range(y)]
    for r in range(0, len(cur)):
        for c in range(0, len(cur[0])):
            num_alive = nearby_organisms(cur, r, c)
            if cur[r][c] == 1 and num_alive < 2 or num_alive > 3:
                col = col_about_to_die
            elif (cur[r][c] == 1 and 2 <= num_alive <= 3) or (cur[r][c] == 0 and num_alive == 3):
                nxt[r][c] = 1
                col = col_alive

            col = col if cur[r][c] == 1 else col_background
            pygame.draw.rect(surface, col, (c * sz, r * sz, sz - 1, sz - 1))

    return nxt


